namespace Calculator
{
    public static class Calculator
    {
        public static decimal SumTwoNumbers(decimal number, decimal otherNumber)
        {
            return number + otherNumber;
        }

        public static decimal SubtractTwoNumbers(decimal number, decimal otherNumber)
        {
            return number - otherNumber;
        }

        public static decimal MultiplyTwoNumbers(decimal number, decimal otherNumber)
        {
            return number * otherNumber;
        }
        
        public static decimal DivideTwoNumbers(decimal number, decimal otherNumber)
        {
            return number / otherNumber;
        }
    }
}
